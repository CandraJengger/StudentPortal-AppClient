package com.jengger.studentportalappclient.ui.jadwal;

public class Jadwal {
    private String kodeMK, namaMK, dosenMK, jamMK, ruangMK;

    public String getKodeMK() {
        return kodeMK;
    }

    public void setKodeMK(String kodeMK) {
        this.kodeMK = kodeMK;
    }

    public String getNamaMK() {
        return namaMK;
    }

    public void setNamaMK(String namaMK) {
        this.namaMK = namaMK;
    }

    public String getDosenMK() {
        return dosenMK;
    }

    public void setDosenMK(String dosenMK) {
        this.dosenMK = dosenMK;
    }

    public String getJamMK() {
        return jamMK;
    }

    public void setJamMK(String jamMK) {
        this.jamMK = jamMK;
    }

    public String getRuangMK() {
        return ruangMK;
    }

    public void setRuangMK(String ruangMK) {
        this.ruangMK = ruangMK;
    }
}
